//saya menggunakan arrow function
const tambah = (p1, p2) => {
    return p1 + p2;
}

const kurang = (p1, p2) => {
    return p1 - p2;
}

const kali = (p1, p2) => {
    return p1 * p2;
}

const bagi = (p1, p2) => {
    return p1 / p2;
}

console.log(tambah(6, 3));
console.log(kurang(6, 3));
console.log(kali(6, 3));
console.log(bagi(6, 3));